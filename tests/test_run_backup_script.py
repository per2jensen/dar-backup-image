def run_script(env_overrides, *args, expect_fail=False):
    """
    Invoke SCRIPT with *args, merging env_overrides into the current environment.

    Returns the CompletedProcess so callers can inspect stdout/stderr.
    When expect_fail=True the assertion is that returncode != 0.
    """
    env = os.environ.copy()
    env.update(env_overrides)
    result = subprocess.run([SCRIPT, *args], env=env, capture_output=True)
    stdout = result.stdout.decode()
    stderr = result.stderr.decode()
    print(f"\n--- STDOUT [{' '.join([SCRIPT, *args])}] ---\n{stdout}")
    print(f"--- STDERR [{' '.join([SCRIPT, *args])}] ---\n{stderr}")
    if expect_fail:
        assert result.returncode != 0, (
            f"Expected non-zero exit but got 0.\nSTDOUT:\n{stdout}\nSTDERR:\n{stderr}"
        )
    else:
        assert result.returncode == 0, (
            f"Script exited {result.returncode}.\nSTDOUT:\n{stdout}\nSTDERR:\n{stderr}"
        )
    return result


def make_dataset(data_dir: Path):
    """Write a small but realistic set of files into data_dir."""
    uid = uuid.uuid4().hex[:6]
    (data_dir / f"hello-{uid}.txt").write_text("Hello World\n")
    (data_dir / f"readme-{uid}.md").write_text("# Test dataset\nGenerated by pytest.\n")
    subprocess.run(
        ["dd", "if=/dev/urandom", f"of={data_dir}/bin-{uid}.bin", "bs=512K", "count=1"],
        check=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL,
    )


def dar_files(backup_dir: Path, definition: str, btype: str) -> list:
    """Return .dar files matching <definition>_<btype>_YYYY-MM-DD.<n>.dar."""
    pat = re.compile(
        rf"^{re.escape(definition)}_{btype}_\d{{4}}-\d{{2}}-\d{{2}}\.\d+\.dar$"
    )
    return [f for f in backup_dir.glob("*.dar") if pat.match(f.name)]


def build_base_env(workdir: Path) -> dict:
    """Minimal environment that run-backup.sh needs."""
    return {
        "WORKDIR": str(workdir),
        "RUN_AS_UID": str(os.getuid()),
        "RUN_AS_GID": str(os.getgid()),
    }


