"""
test_run_backup_script.py
=========================
Tests focused on scripts/run-backup.sh covering:

  1. WORKDIR-based default layout
  2. Explicit per-directory overrides (DAR_BACKUP_DIR, DAR_BACKUP_D_DIR,
     DAR_BACKUP_DATA_DIR, DAR_BACKUP_RESTORE_DIR)
  3. Failure paths — exit code ≠ 0 and meaningful error messages

Run with:
    pytest tests/test_run_backup_script.py -v

Prerequisites:
    - scripts/run-backup.sh is executable and on the path relative to CWD
    - The Docker image referenced by IMAGE env var (default: dar-backup:dev) is present
    - pytest, and the utils module (sha256sum, compare_to_originals) are available
"""

import os
import re
import shutil
import subprocess
import uuid
from pathlib import Path

import pytest

from utils import sha256sum

SCRIPT = "scripts/run-backup.sh"

DEFAULT_BACKUP_DEFINITION = """\
-am
-R /data
-z5
-n
--slice 7G
--cache-directory-tagging
"""


# ---------------------------------------------------------------------------
