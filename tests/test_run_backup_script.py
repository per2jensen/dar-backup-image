class TestExplicitDirectoryOverrides:
    """Every directory can be set independently, overriding WORKDIR defaults."""

    def _env(self, dirs: dict) -> dict:
        """Build the env dict from the dirs fixture."""
        return {
            "DAR_BACKUP_DIR":         str(dirs["backups"]),
            "DAR_BACKUP_D_DIR":       str(dirs["backup_d"]),
            "DAR_BACKUP_DATA_DIR":    str(dirs["data"]),
            "DAR_BACKUP_RESTORE_DIR": str(dirs["restore"]),
            "RUN_AS_UID":             str(os.getuid()),
            "RUN_AS_GID":             str(os.getgid()),
            # Provide a WORKDIR that is *different* from the explicit dirs
            # so we can confirm the overrides win.
            "WORKDIR": "/tmp/should-not-be-used",
        }

    def test_full_backup_honours_dar_backup_dir(self, explicit_dirs_env):
        """DAR archives land in DAR_BACKUP_DIR, not the default WORKDIR/backups."""
        env = self._env(explicit_dirs_env)
        run_script(env, "-t", "FULL")
        assert any(explicit_dirs_env["backups"].glob("*.dar")), \
            "Expected .dar files in the explicit DAR_BACKUP_DIR"
        # The would-be default path must NOT have been used
        assert not Path("/tmp/should-not-be-used/backups").exists() or \
               not any(Path("/tmp/should-not-be-used/backups").glob("*.dar")), \
               "Archives appeared in WORKDIR/backups instead of DAR_BACKUP_DIR"

    def test_full_diff_incr_chain_with_explicit_dirs(self, explicit_dirs_env):
        """Complete chain works when all four dirs are explicitly set."""
        env = self._env(explicit_dirs_env)
        data = explicit_dirs_env["data"]
        backups = explicit_dirs_env["backups"]

        run_script(env, "-t", "FULL")
        assert dar_files(backups, "default", "FULL")

        (data / "new_for_diff.txt").write_text("diff data\n")
        run_script(env, "-t", "DIFF")
        assert dar_files(backups, "default", "DIFF")

        (data / "new_for_incr.txt").write_text("incr data\n")
        run_script(env, "-t", "INCR")
        assert dar_files(backups, "default", "INCR")

    def test_custom_backup_definition_via_override(self, explicit_dirs_env):
        """A named definition in DAR_BACKUP_D_DIR is found and used."""
        env = self._env(explicit_dirs_env)
        def_name = "myproject"
        (explicit_dirs_env["backup_d"] / def_name).write_text(
            "-am\n-R /data\n-z1\n--slice 1G\n"
        )
        run_script(env, "-t", "FULL", "-d", def_name)
        assert dar_files(explicit_dirs_env["backups"], def_name, "FULL"), \
            f"No FULL .dar files for definition '{def_name}'"

    def test_only_dar_backup_dir_overridden(self, workdir_env):
        """
        Overriding just DAR_BACKUP_DIR redirects archive output while the other
        three dirs remain under WORKDIR.
        """
        custom_backups = workdir_env.parent / "custom_backups_only"
        custom_backups.mkdir(parents=True, exist_ok=True)
        env = build_base_env(workdir_env)
        env["DAR_BACKUP_DIR"] = str(custom_backups)
        run_script(env, "-t", "FULL")
        assert any(custom_backups.glob("*.dar")), \
            "Expected archives in the overridden DAR_BACKUP_DIR"
        assert not any((workdir_env / "backups").glob("*.dar")), \
            "Archives must not appear in the default WORKDIR/backups"

    def test_only_data_dir_overridden(self, workdir_env, tmp_path):
        """
        Overriding DAR_BACKUP_DATA_DIR sources from that path, not WORKDIR/data.
        """
        custom_data = tmp_path / "special_data"
        custom_data.mkdir()
        make_dataset(custom_data)
        env = build_base_env(workdir_env)
        env["DAR_BACKUP_DATA_DIR"] = str(custom_data)
        run_script(env, "-t", "FULL")
        assert any((workdir_env / "backups").glob("*.dar")), \
            "Expected archives even when using a custom data dir"

    def test_script_echoes_explicit_dir_values(self, explicit_dirs_env):
        """Each overridden path should appear somewhere in the script's output."""
        env = self._env(explicit_dirs_env)
        result = run_script(env, "-t", "FULL")
        stdout = result.stdout.decode()
        for key, path in explicit_dirs_env.items():
            assert str(path) in stdout, \
                f"Expected '{path}' ({key}) to appear in script output"


