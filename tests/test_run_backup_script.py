class TestWorkdirLayout:
    """Behaviour when only WORKDIR is set; all sub-dirs default from it."""

    def test_full_backup_creates_dar_files(self, workdir_env):
        """FULL backup via WORKDIR produces at least one .dar in workdir/backups."""
        env = build_base_env(workdir_env)
        run_script(env, "-t", "FULL")
        assert any((workdir_env / "backups").glob("*.dar")), \
            "Expected .dar files in workdir/backups after FULL backup"

    def test_full_diff_incr_chain(self, workdir_env):
        """
        WORKDIR layout supports a complete FULL → DIFF → INCR chain,
        each stage producing its own .dar file set.
        """
        env = build_base_env(workdir_env)
        data = workdir_env / "data"
        backups = workdir_env / "backups"

        run_script(env, "-t", "FULL")
        assert dar_files(backups, "default", "FULL"), "No FULL .dar files"

        (data / "delta.txt").write_text("change for DIFF\n")
        run_script(env, "-t", "DIFF")
        assert dar_files(backups, "default", "DIFF"), "No DIFF .dar files"

        (data / "delta2.txt").write_text("change for INCR\n")
        run_script(env, "-t", "INCR")
        assert dar_files(backups, "default", "INCR"), "No INCR .dar files"

    def test_script_prints_workdir_in_output(self, workdir_env):
        """The script should echo the base directory it is using."""
        env = build_base_env(workdir_env)
        result = run_script(env, "-t", "FULL")
        stdout = result.stdout.decode()
        assert str(workdir_env) in stdout, \
            "Expected WORKDIR path to appear in script output"

    def test_missing_type_flag_fails_with_usage_hint(self, workdir_env):
        """
        Omitting -t must exit non-zero and the output must contain a usage hint
        so the operator knows what went wrong.
        """
        env = build_base_env(workdir_env)
        result = run_script(env, expect_fail=True)
        combined = result.stdout.decode() + result.stderr.decode()
        assert "FULL" in combined or "usage" in combined.lower() or "Usage" in combined, \
            "Error output should contain a usage/help hint mentioning FULL|DIFF|INCR"

    def test_workdir_subdirectories_are_auto_created(self, tmp_path):
        """
        When WORKDIR points at a directory without sub-dirs, the script must
        create backups/, backup.d/, data/, restore/ automatically.
        """
        workdir = tmp_path / "auto_create"
        workdir.mkdir()
        # Write definition and data manually because script won't have data to back up otherwise
        (workdir / "backup.d").mkdir()
        (workdir / "backup.d" / "default").write_text(DEFAULT_BACKUP_DEFINITION)
        (workdir / "data").mkdir()
        make_dataset(workdir / "data")
        env = build_base_env(workdir)
        run_script(env, "-t", "FULL")
        for sub in ("backups", "backup.d", "data", "restore"):
            assert (workdir / sub).is_dir(), f"Expected {sub}/ to exist after run"

    def test_default_definition_file_is_created_if_absent(self, tmp_path):
        """
        If backup.d/default does not exist the script must create it and still
        succeed.
        """
        workdir = tmp_path / "nodef"
        for sub in ("backups", "backup.d", "data", "restore"):
            (workdir / sub).mkdir(parents=True)
        make_dataset(workdir / "data")
        env = build_base_env(workdir)
        # No default definition written intentionally
        run_script(env, "-t", "FULL")
        assert (workdir / "backup.d" / "default").exists(), \
            "Script should auto-create backup.d/default"

    def test_case_insensitive_backup_type(self, tmp_path):
        """
        Backup type flags should be accepted in any case (full, Full, FULL).

        Each variant gets its own isolated workdir because dar-backup enforces
        a "one FULL per day" rule: re-running the same type in the same backup
        directory on the same day exits non-zero.  Using separate directories
        keeps the runs independent and the test honest.
        """
        for variant in ("full", "Full", "FULL"):
            workdir = tmp_path / f"case_{variant}"
            for sub in ("backups", "backup.d", "data", "restore"):
                (workdir / sub).mkdir(parents=True)
            (workdir / "backup.d" / "default").write_text(DEFAULT_BACKUP_DEFINITION)
            make_dataset(workdir / "data")
            env = build_base_env(workdir)
            run_script(env, "-t", variant)

    def test_incremental_longform_alias(self, workdir_env):
        """--type incremental is accepted as an alias for INCR."""
        env = build_base_env(workdir_env)
        data = workdir_env / "data"
        backups = workdir_env / "backups"

        run_script(env, "-t", "FULL")
        (data / "extra.txt").write_text("for incr\n")
        run_script(env, "-t", "DIFF")
        (data / "extra2.txt").write_text("for incr 2\n")
        run_script(env, "-t", "incremental")   # long alias
        assert dar_files(backups, "default", "INCR"), \
            "No INCR .dar files when using 'incremental' alias"


