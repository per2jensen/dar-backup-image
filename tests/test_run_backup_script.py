class TestFailurePaths:
    """
    Each test verifies that the script exits non-zero AND that the error
    output contains a meaningful message — not a cryptic shell trace.
    """

    def _combined(self, result) -> str:
        return result.stdout.decode() + result.stderr.decode()

    # --- Argument / option errors ---

    def test_missing_t_flag(self, workdir_env):
        """No -t flag → non-zero exit with usage line."""
        env = build_base_env(workdir_env)
        result = run_script(env, expect_fail=True)
        msg = self._combined(result)
        assert "FULL" in msg or "-t" in msg, \
            "Should mention -t / FULL|DIFF|INCR in error output"

    def test_t_flag_without_value(self, workdir_env):
        """-t with no following value must exit non-zero with a clear complaint."""
        env = build_base_env(workdir_env)
        result = run_script(env, "-t", expect_fail=True)
        msg = self._combined(result)
        # Bash will surface either an argument error or usage
        assert result.returncode != 0

    def test_invalid_backup_type(self, workdir_env):
        """An unrecognised -t value must exit non-zero and name the bad value."""
        env = build_base_env(workdir_env)
        result = run_script(env, "-t", "WEEKLY", expect_fail=True)
        msg = self._combined(result)
        assert "WEEKLY" in msg or "Invalid" in msg or "invalid" in msg, \
            "Error output should identify the invalid type"

    def test_unknown_option(self, workdir_env):
        """An unknown flag like --frobnicate must exit non-zero."""
        env = build_base_env(workdir_env)
        result = run_script(env, "--frobnicate", expect_fail=True)
        msg = self._combined(result)
        assert "frobnicate" in msg or "Unknown" in msg or "unknown" in msg, \
            "Error output should identify the unknown option"

    def test_d_flag_without_value(self, workdir_env):
        """-d with no value must exit non-zero with a helpful message."""
        env = build_base_env(workdir_env)
        result = run_script(env, "-t", "FULL", "-d", expect_fail=True)
        assert result.returncode != 0

    def test_nonexistent_definition_file(self, workdir_env):
        """Specifying a definition that does not exist must fail clearly."""
        env = build_base_env(workdir_env)
        result = run_script(env, "-t", "FULL", "-d", "ghost-definition", expect_fail=True)
        # Just non-zero exit is the minimum guarantee from the script;
        # dar-backup itself will surface the missing file error.
        assert result.returncode != 0

    # --- UID/root guard ---

    def test_root_uid_is_rejected(self, workdir_env):
        """RUN_AS_UID=0 must exit non-zero with a message about root not being allowed."""
        env = build_base_env(workdir_env)
        env["RUN_AS_UID"] = "0"
        result = run_script(env, "-t", "FULL", expect_fail=True)
        msg = self._combined(result)
        assert "root" in msg.lower(), \
            "Error message should mention 'root' when UID 0 is rejected"

    # --- Docker image errors ---

    def test_nonexistent_docker_image_fails(self, workdir_env):
        """Pointing IMAGE at a non-existent tag must fail (docker can't find it)."""
        env = build_base_env(workdir_env)
        env["IMAGE"] = "dar-backup:this-tag-does-not-exist-at-all"
        env["DOCKER_PULL"] = "false"
        result = run_script(env, "-t", "FULL", expect_fail=True)
        assert result.returncode != 0

    # --- Directory / path guard ---

    def test_root_slash_as_backup_dir_is_rejected(self, workdir_env):
        """DAR_BACKUP_DIR=/ must be refused before any Docker call."""
        env = build_base_env(workdir_env)
        env["DAR_BACKUP_DIR"] = "/"
        result = run_script(env, "-t", "FULL", expect_fail=True)
        msg = self._combined(result)
        assert "/" in msg and ("Refusing" in msg or "refusing" in msg or result.returncode != 0), \
            "Script should refuse to use / as backup dir"

    def test_root_slash_as_data_dir_is_rejected(self, workdir_env):
        """DAR_BACKUP_DATA_DIR=/ must be refused before any Docker call."""
        env = build_base_env(workdir_env)
        env["DAR_BACKUP_DATA_DIR"] = "/"
        result = run_script(env, "-t", "FULL", expect_fail=True)
        assert result.returncode != 0

    # --- Positional argument noise ---

    def test_unexpected_positional_argument(self, workdir_env):
        """Stray positional arguments after valid flags must cause a non-zero exit."""
        env = build_base_env(workdir_env)
        result = run_script(env, "-t", "FULL", "unexpected_extra", expect_fail=True)
        msg = self._combined(result)
        assert "unexpected" in msg.lower() or result.returncode != 0, \
            "Script should reject unexpected positional arguments"

    # --- Exit code is exactly non-zero (not just non-zero) ---

    def test_exit_code_is_1_for_missing_t(self, workdir_env):
        """
        The script should exit with code 1 (not e.g. 127 or 2) for a usage
        error so callers can distinguish it from 'command not found'.
        """
        env = build_base_env(workdir_env)
        env_full = os.environ.copy()
        env_full.update(env)
        result = subprocess.run([SCRIPT], env=env_full, capture_output=True)
        assert result.returncode == 1, \
            f"Expected exit code 1 for missing -t, got {result.returncode}"

    def test_exit_code_is_1_for_root_uid(self, workdir_env):
        """Root UID rejection should also exit with code 1."""
        env = build_base_env(workdir_env)
        env["RUN_AS_UID"] = "0"
        env_full = os.environ.copy()
        env_full.update(env)
        result = subprocess.run([SCRIPT, "-t", "FULL"], env=env_full, capture_output=True)
        assert result.returncode == 1, \
            f"Expected exit code 1 for root UID, got {result.returncode}"

    # --- WORKDIR guard ---

    def test_fail_when_workdir_not_set(self):
        """
        An unset WORKDIR must exit non-zero with a message that mentions WORKDIR,
        so the operator immediately knows what to fix.
        """
        env = os.environ.copy()
        env.pop("WORKDIR", None)
        env["RUN_AS_UID"] = str(os.getuid())
        env["RUN_AS_GID"] = str(os.getgid())
        result = subprocess.run([SCRIPT, "-t", "FULL"], env=env, capture_output=True)
        assert result.returncode != 0, \
            "Script must exit non-zero when WORKDIR is not set"
        msg = result.stdout.decode() + result.stderr.decode()
        assert "WORKDIR" in msg, \
            "Error output must mention 'WORKDIR' so the operator knows what to fix"

    def test_exit_code_is_1_for_missing_workdir(self):
        """WORKDIR not set should exit with code 1, consistent with other guard exits."""
        env = os.environ.copy()
        env.pop("WORKDIR", None)
        env["RUN_AS_UID"] = str(os.getuid())
        env["RUN_AS_GID"] = str(os.getgid())
        result = subprocess.run([SCRIPT, "-t", "FULL"], env=env, capture_output=True)
        assert result.returncode == 1, \
            f"Expected exit code 1 for missing WORKDIR, got {result.returncode}"


